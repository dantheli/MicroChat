import UIKit

extension UIColor {
    static func navigationBar() -> UIColor {
        return UIColor(red: 1.0, green: 1.0, blue: 1.0, alpha: 1.0)
    }
    
    static func microPurple() -> UIColor {
        return UIColor.purpleColor()
    }
}